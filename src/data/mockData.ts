// Helper function to get customer by ID
export const getCustomerById = (id: string): Customer | undefined => {
  return customers.find(c => c.id === id);
};

// Helper function to get salesperson by ID
export const getSalespersonById = (id: string): Salesperson | undefined => {
  return salespeople.find(s => s.id === id);
};

// Helper function to get loads by salesperson ID
export const getLoadsBySalesperson = (salespersonId: string): Load[] => {
  return loads.filter(l => l.salespersonId === salespersonId);
};

// Helper function to get loads by customer ID
export const getLoadsByCustomer = (customerId: string): Load[] => {
  return loads.filter(l => l.customerId === customerId);
};

// Helper function to get customers by salesperson ID
export const getCustomersBySalesperson = (salespersonId: string): Customer[] => {
  const customerIds = new Set(
    loads.filter(l => l.salespersonId === salespersonId).map(l => l.customerId)
  );
  return customers.filter(c => customerIds.has(c.id));
};
